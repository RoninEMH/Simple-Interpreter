package Interpreter;

public class Couple<T, K> {

	private T first;
	private K second;

	public T getFirst() {
		return this.first;
	}

	public void setFirst(T first) {
		this.first = first;
	}

	public K getSecond() {
		return this.second;
	}

	public void setSecond(K second) {
		this.second = second;
	}

}
